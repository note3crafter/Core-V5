namespace TheNote\core\command;

use TheNote\core\Main;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\Player;
use pocketmine\utils\Config;

class HealCommand extends Command {

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        parent::__construct("heal", $config->get("prefix") . "§eHeilt §6dich", "/heal");
        $this->setPermission("core.command.heal");
    }
    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        if (!$sender instanceof Player) {
            $sender->sendMessage($config->get("error") . "§cDiesen Command kannst du nur Ingame benutzen");
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($config->get("error") . "Du hast keine Berechtigung um diesen Command auszuführen!");
            return false;
        }
        $nickname = $sender->getNameTag();
        if ($sender->hasPermission("core.command.heal") || $sender->isOp()) {
            $sender->setHealth(20);
            $sender->sendMessage($config->get("info") . "Du wurdest §eGeheilt§6.");
            $this->plugin->getServer()->broadcastMessage($config->get("info") . "§c$nickname §6hat sich geheilt!");
        }
        return false;
    }
}