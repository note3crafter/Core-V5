namespace TheNote\core\command;

use pocketmine\utils\Config;
use pocketmine\utils\TextFormat;
use TheNote\core\Main;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\Player;

class RenameCommand extends Command
{

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        parent::__construct("rename", $config->get("prefix") . "§6Benenne ein Item Um", "/rename <itemname>");
        $this->setPermission("core.command.rename");
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        if (!$sender instanceof Player) {
            $sender->sendMessage($config->get("error") . "§cDiesen Command kannst du nur Ingame benutzen");
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($config->get("error") . "Du hast keine Berechtigung um diesen Command auszuführen!");
            return false;
        }
        if (empty($args)) {
            $sender->sendMessage($this->getUsage());
            return true;
        }

        $item = $sender->getInventory()->getItemInHand();
        if ($item === null) {
            $sender->sendMessage($config->get("error") . "Du hast kein Item in der Hand!");
            return true;
        }
        $item->clearCustomName();
        $item->setCustomName(str_replace("&", TEXTFORMAT::ESCAPE, $args[0]));
        $sender->getInventory()->setItemInHand($item);
        $sender->sendMessage($config->get("prefix") . "Du hast dein Item erfolgreich umbenannt");
        return true;
    }
}


