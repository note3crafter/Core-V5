namespace TheNote\core\server\generators\normal\biome;

use pocketmine\block\Block;
use TheNote\core\server\generators\normal\biome\types\Biome;

class FrozenOcean extends Biome {

    public function __construct() {
        parent::__construct(0, 0.5);

        $this->setGroundCover([
            Block::get(Block::GRAVEL),
            Block::get(Block::GRAVEL)
        ]);

        $this->setElevation(55, 57);
    }

    public function isFrozen(): bool {
        return true;
    }

    public function getName(): string {
        return "Frozen Ocean";
    }
}