namespace TheNote\core\blocks\redstone;

use pocketmine\Player;
use pocketmine\item\Item;

class DaylightDetectorInverted extends DaylightDetector {
    
    protected $id = self::DAYLIGHT_DETECTOR_INVERTED;
    
    public function onActivate(Item $item, Player $player = null) : bool {
        $this->getLevel()->setBlock($this, new DaylightDetector(), true, true);
        $this->getLevel()->getBlock($this)->updatePower();
        return true;
    }
}